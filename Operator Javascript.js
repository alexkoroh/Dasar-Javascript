// ! Operator dalam Jvascript ...(Operator Aritmatika)

let a = 3 + 2 // disini operator aritmatika adalah tanda +
let b = 3 - 2 // disini operator aritmatika adalah tanda -
let c = 3 * 2 // disini operator aritmatika adalah tanda *
let d = 3 / 2 // disini operator aritmatika adalah tanda /
