//! Pada umumnya bahasa pemrograman seperti c++ , harus mendeklarasikan tipe datanya seperti ( int , float,..)
//! Tapi dalam javascript , kita tidak perlu melakukan itu...karena javascript otomatis menentukan tipe data untuk kita

//*Tipe data Number
let umur = 21
console.log(`tipe data umur = ${typeof(umur)}`); // Ouputnya number

//* Tipe data String
let nama ="alex koroh"
console.log(`tipe data nama = ${typeof(nama)}`); //String

//* Tipe data Float
let Menikah = true;
console.log(`tipe data Menikah = ${typeof(Menikah)}`); 

//* Tipe data Array
let Nama = ["alex","gerri","Tedi","rey"]
console.log(`tipe data Nama = ${typeof(Nama)}`); 

